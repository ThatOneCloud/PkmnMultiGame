package net.cloud.gfx.elements;

/**
 * Alignment of a graphic element. 
 * Not supported by many, as it's somewhat unusual in terms of how interfaces are usually constructed.
 */
public enum Alignment {
	
	/** Aligned on center lines, vertical and/or horizontal depending on the context */
	CENTER,
	
	/** Aligned on the left edge */
	LEFT,
	
	/** Aligned on the right edge */
	RIGHT,
	
	/** Aligned on the top edge */
	TOP,
	
	/** Aligned on the bottom edge */
	BOTTOM;

}
